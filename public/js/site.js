const attachContactForm = () => {
  const form = document.getElementById('contactForm');
  if (!form || isContactFormBound) {
    return;
  }

  isContactFormBound = true;

  form.addEventListener('submit', async (event) => {
    event.preventDefault();

    const status = document.getElementById('contactFormStatus');
    const copy = translations[currentLang].contact;

    const payload = {
      name: document.getElementById('contactName').value.trim(),
      phone: document.getElementById('contactPhone').value.trim(),
      serviceType: document.getElementById('contactServiceType').value,
      location: document.getElementById('contactLocation').value.trim(),
      preferredDate: document.getElementById('contactPreferredDate').value.trim(),
      message: document.getElementById('contactMessage').value.trim(),
    };

    try {
      status.textContent = '';

      const response = await fetch('/api/contact', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify(payload),
      });

      if (!response.ok) {
        throw new Error('Failed to submit');
      }

      form.reset();
      status.textContent = copy.success;
    } catch (error) {
      status.textContent = copy.error;
    }
  });
};

const renderPage = () => {
  try {
    applyDirection();
    renderCommon();

    if (page === 'home') {
      renderHomePage();
    }

    if (page === 'contact') {
      renderContactPage();
      attachContactForm();
    }
  } catch (error) {
    console.error('Render error:', error);
  }
};

const init = async () => {
  try {
    const response = await fetch('/api/public/settings');

    if (response.ok) {
      const data = await response.json();
      siteSettings = data && typeof data === 'object' ? data : defaultSiteSettings;
    } else {
      console.error('Settings API returned error, using fallback defaults');
      siteSettings = defaultSiteSettings;
    }
  } catch (error) {
    console.error('Settings API failed, using fallback defaults', error);
    siteSettings = defaultSiteSettings;
  }

  renderPage();
};

