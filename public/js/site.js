const renderSocialLinks = (container, labels) => {
  if (!container) {
    return;
  }

  container.innerHTML = '';

  Object.entries(siteSettings.socials || {}).forEach(([platform, details]) => {
    if (!details?.enabled || !details?.url) {
      return;
    }

    const link = document.createElement('a');
    link.href = details.url;
    link.target = '_blank';
    link.rel = 'noreferrer';
    link.textContent = labels[platform] || platform;
    container.appendChild(link);
  });
};

